# int = números inteiros positivos ou negativos
idade = 47
temperatura = -64
ano = 1979

print(idade)
print(temperatura)
print(ano)

# Números inteiros podem ser usados para realizar cálculos
soma = idade + ano
print(soma)

# Números inteiros podem ser usados em comparações
if idade > 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")

# Números inteiros podem ser utilizados para contagens
for i in range(47):
    print(i)

# Números inteiros podem ser utilizados para indexação
lista = [1, 2, 3, 4, 5]
print(lista[0])     # imprime o primeiro elemento da lista
print(lista[1])     # imprime o segundo elemento da lista
print(lista[2])     # imprime o terceiro elemento da lista
print(lista[3])     # imprime o quarto elemento da lista
print(lista[4])     # imprime o quinto elemento da lista
