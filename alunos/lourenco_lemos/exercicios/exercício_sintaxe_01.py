# 1. Print - Exibir Mensagens

# Usa-se o comando print para exibir mensagens ou valores na tela. Exemplo:
print("Olá, mundo!")
# Pode imprimir números
print(154)
# Pode imprimir múltiplos valores separados por vírgula
print("Meu nome é", "Lourenço", "e tenho", 43, "anos")
# Resultado: Meu nome é Lourenço e tenho 43 anos